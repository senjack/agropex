/*[1.] : DESIGNING A HOVER EFFECT*/
function hvr(a){
  var b = window.document.getElementById(a)
  b.style.color = "orange";
  b.style.background = "#fff";
  b.style.borderRadius = "5px";
}
function out(a){
  var b = window.document.getElementById(a)
  b.style.color = "#fff";
  b.style.background = "";
}